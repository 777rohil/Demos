    private void menuSaveReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuSaveReportActionPerformed
        // TODO add your handling code here:
        // Method that is called when the save report button is clicked 
        try(BufferedWriter writer = new BufferedWriter(new FileWriter("report.txt"))){
            System.out.println("ESTATE AGENT REPORT\n" + "*".repeat(20));
            writer.write(txtReportArea.getText());
            System.out.println("*".repeat(20));
            JOptionPane.showMessageDialog(null, "Report Saved Successfully to report.txt", "Success", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error saving file: " + e.getMessage(), "Save Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_menuSaveReportActionPerformed

