    private void menuProcessReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuProcessReportActionPerformed
        // TODO add your handling code here:
        String location = (String) jComboBox.getSelectedItem();
        String agentName = txtAgentName.getText();
        String priceStr = txtPropertyPrice.getText();
        String percentageStr = txtCommissionPercentage.getText();
        
        // Parse the numeric values (with basic error handling)
        try{
            double propertyPrice = Double.parseDouble(priceStr);
            double commissionPercentage = Double.parseDouble(percentageStr);
            
            //Calculate Commission
            double commission = propertyPrice * (commissionPercentage / 100);
            
            //Display Report
            String report = 
                     "AGENT LOCATION: " + location + "\n" 
                    + "AGENT NAME: " + agentName + "\n"
                    +"PROPERTY PRICE: R" + propertyPrice + "\n"
                    + "COMMISSION PERCENTAGE: " + commissionPercentage + "%\n" 
                    + "CALCULATED COMMISSION: R" + commission + "\n";
                    
            
            txtReportArea.setText(report);
        }catch(NumberFormatException e){
            // When users enter non-numeric data for price / percentage
            JOptionPane.showMessageDialog(null, "Please Enter Valid Numbers For Price & Percentage", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_menuProcessReportActionPerformed

